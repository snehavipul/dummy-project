package assignment_3;

public class OperationLogic {
	
	public int addition(int a, int b) {
		
		int c=a+b;
		return c;
	}
	
public int substraction(int p, int q) {
		
		int r=p-q;
		return r;
	}

public int multiplication(int x, int y) {
	
	int z=x*y;
	return z;
	}

public int division(int m, int n) {
	
	int H=m/n;
	return H;
	
}
}
